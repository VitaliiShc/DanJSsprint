function initializeBoard(cards) {
  cards.forEach(function (card) {
    const randomPos = Math.floor(Math.random() * CARDS_COUNT);
    card.style.order = `${randomPos}`;
    card.addEventListener('click', cardClickHandler, { once: true });
  });
}

